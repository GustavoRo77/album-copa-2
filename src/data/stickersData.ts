// Função auxiliar para obter figurinhas por grupo
export const getStickersByGroup = (group: string) => {
  const groups: { [key: string]: number[] } = {
    'A': [1, 2, 3, 4, 5, 6],
    'B': [7, 8, 9, 10, 11, 12],
    'C': [13, 14, 15, 16, 17, 18],
    'D': [19, 20, 21, 22, 23, 24]
  }
  
  const ids = groups[group] || []
  return stickers.filter(s => ids.includes(s.id))
}

// Função para obter estatísticas
export const getAlbumStats = (stickersList: typeof stickers) => {
  const total = stickersList.length
  const collected = stickersList.filter(s => s.coletada).length
  const percentage = total > 0 ? Math.round((collected / total) * 100) : 0
  
  return { total, collected, percentage }
}